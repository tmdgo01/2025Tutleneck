                    offset: const Offset(0, 2),
                  ),
                ],
              ),
              child: Column(
                children: [
                  const Text(
                    'Ï£ºÍ∞Ñ ÌèâÍ∑† ÏûêÏÑ∏ Ï†êÏàò',
                    style: TextStyle(fontSize: 16, fontWeight: FontWeight.w600),
                  ),
                  const SizedBox(height: 8),
                  Text(
                    '${_weeklyAverage.toStringAsFixed(1)}Ï†ê',
                    style: TextStyle(
                      fontSize: 32,
                      fontWeight: FontWeight.bold,
